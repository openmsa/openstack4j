package org.openstack4j.model.zun;

public class MountsBuilder {

    private String source;

    private String destination;

    public MountsBuilder(String source, String destination) {
        super();
        this.source = source;
        this.destination = destination;
    }

    public static MountsBuilder of(String source, String destination) {
        return new MountsBuilder(source, destination);
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

}
